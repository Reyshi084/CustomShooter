    // x, y で指定された敵の配置データを返す
    public static int GetStageData(int stageNum, int x, int y)
    {
        return StageDataList[stageNum][y, x];
    }

    // 配置の横の長さを返す
    public static int GetStageLengthX(int stageNum)
    {
        return StageDataList[stageNum].GetLength(1);
    }

    // 配置の縦の長さを返す
    public static int GetStageLengthY(int stageNum)
    {
        return StageDataList[stageNum].GetLength(0);
    }

    // 弾発射のインターバル時間を返す
    public static float GetBulletInterval(int stageNum)
    {
        return BulletInterval[stageNum];
    }

    // 敵の動く速度基準を返す
    public static float GetEnemyStandardSpeed(int stageNum)
    {
        return EnemyStandardSpeed[stageNum];
    }

    public static int GetExp(int stageNum)
    {
        return Exp[stageNum];
    }

    // 敵の数を返す
    public static int GetEnemyNum(int stageNum)
    {
        var xlen = GetStageLengthX(stageNum);
        var ylen = GetStageLengthY(stageNum);

        int cnt = 0;

        for(int y = 0; y < ylen; y++)
        {
            for(int x = 0; x < xlen; x++)
            {
                if(GetStageData(stageNum, x, y) != 0)
                {
                    cnt++;
                }
            }
        }

        return cnt;
    }

    public static string GetStageName(int stageNum)
    {
        if (stageNum >= StageName.Length) // 実装外のステージ番号
            return "未実装";
        else if (stageNum <= PlayerData.STAGE) // 解禁されたステージ番号
            return StageName[stageNum];
        else
            return "未解禁"; // 解禁前のステージ番号
    }
